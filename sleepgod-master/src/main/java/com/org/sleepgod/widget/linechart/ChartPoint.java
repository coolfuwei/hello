public class ChartPoint {
    private int x;
    private int y;
    private int xData;
    private int yData;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getxData() {
        return xData;
    }

    public void setxData(int xData) {
        this.xData = xData;
    }

    public int getyData() {
        return yData;
    }

    public void setyData(int yData) {
        this.yData = yData;
    }
}
